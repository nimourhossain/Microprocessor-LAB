include 'emu8086.inc'        ; EMU8086 লাইব্রেরি যোগ করছে, যেটা ইনপুট-আউটপুট সহজ করে

org 100h                     ; প্রোগ্রাম মেমোরি 100h ঠিকানা থেকে শুরু হবে (.COM ফাইলের জন্য)

jmp start                    ; সরাসরি start লেবেলে গিয়ে কোড চালাবে

start:
    print "Enter our Number: " ; ইউজারকে সংখ্যা দিতে বলছে

    call scan_num              ; ইউজারের দেয়া সংখ্যা পড়ে CX রেজিস্টারে রাখবে
    mov ax, cx                 ; CX থেকে AX রেজিস্টারে সংখ্যা কপি করছে
    and ax, 1                  ; AX এর সবচেয়ে কম গুরুত্বপুর্ন বিট (LSB) বের করছে (সংখ্যা জোড় নাকি বিজোড় যাচাই করতে)
    cmp ax, 0                  ; যদি LSB 0 হয়, তাহলে সংখ্যা জোড়
    je even                    ; যদি জোড় হয়, তাহলে even লেবেলে যাবে

    print "This Number is Odd" ; সংখ্যা বিজোড় হলে এটা দেখাবে
    jmp done                   ; প্রোগ্রাম শেষের অংশে যাবে

even:
    print "This Number is Even" ; সংখ্যা জোড় হলে এটা দেখাবে

done:
    printn                     ; নতুন লাইনে যাবে
    mov ah, 4ch                ; প্রোগ্রাম শেষ করার জন্য DOS কল প্রস্তুত করছে
    int 21h                    ; প্রোগ্রাম বন্ধ করবে

define_scan_num               ; scan_num ফাংশনের কোড এখানে থাকবে (ইনপুট নেওয়ার জন্য)
define_print_num              ; print_num ফাংশনের কোড এখানে থাকবে (সংখ্যা দেখানোর জন্য)
define_print_num_uns          ; unsigned সংখ্যা দেখানোর জন্য ফাংশন (ব্যবহৃত হয়নি)
define_get_string             ; স্ট্রিং ইনপুট ফাংশন (ব্যবহৃত হয়নি)

end start                    ; প্রোগ্রামের শুরু হওয়ার জায়গা নির্দেশ করছে
